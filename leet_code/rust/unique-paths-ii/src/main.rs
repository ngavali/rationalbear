struct Solution {}

impl Solution {
    fn dfs(obstacle_grid: &Vec<Vec<i32>>, x: usize, y: usize, m: usize, n: usize, memo: &mut Vec<Vec<i32>>) -> i32 {
        //println!("At position: x={} y={} (m={} n={})", x, y, m, n);
        let mut res = 0;
        if (obstacle_grid[x][y] == 1) {
            return res;
        }
        if (x == m - 1 && y == n - 1) {
            //println!("Reached !!!");
            return 1;
        }
        if memo[x][y] != -1 {
            return memo[x][y];
        }
        if (x < m - 1) {
            res += Self::dfs(obstacle_grid, x + 1, y, m, n, memo);
        }
        if (y < n - 1) {
            res += Self::dfs(obstacle_grid, x, y + 1, m, n, memo);
        }
        memo[x][y] = res;
        res
    }
    pub fn unique_paths_with_obstacles(obstacle_grid: Vec<Vec<i32>>) -> i32 {
        let (m,n) = (obstacle_grid.len(), obstacle_grid[0].len());
        let mut memo = vec![vec![-1; n]; m];
        println!("{:?}", memo);
        Self::dfs(
            &obstacle_grid,
            0,
            0,
            m, n,
            &mut memo,
        )
    }
}

