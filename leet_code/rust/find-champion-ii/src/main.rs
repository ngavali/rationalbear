//https://leetcode.com/problems/find-champion-ii/
struct Solution;

impl Solution {
    pub fn find_champion(n: i32, edges: Vec<Vec<i32>>) -> i32 {
        let mut indegree: Vec<bool> = vec![false;n as usize];
        for edge in edges {
            indegree[edge[1] as usize] = true;
        }
        let nonzero_indegree : Vec<(usize, &bool)> = indegree.iter().enumerate().filter(|(i, v)| !*v ).collect();
        match nonzero_indegree.len() {
            1 => nonzero_indegree[0].0 as i32,
            _ => -1
        }
    }
    //Alternate
    //Is this faster?
    //Is enumerate loop slower than 2 loops below?
    pub fn find_champion_alt(n: i32, edges: Vec<Vec<i32>>) -> i32 {
        let mut lost = vec![false; n as u32 as usize].into_boxed_slice();

        edges
            .into_iter()
            .for_each(|edge| lost[edge[1] as u32 as usize] = true);

        if lost.iter().filter(|&&lost| !lost).count() == 1 {
            lost.iter().position(|&lost| !lost).unwrap() as _
        } else {
            -1
        }
    }
}
